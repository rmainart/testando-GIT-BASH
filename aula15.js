// Loop

// 1. For
// 2. While
// 3. Do..While
// 4. For..In
// 5. For..of


//crecente
for (let i = 1; i <= 5; i++){
    if (i % 2 !== 0 ){ // somente impares
        console.log('Estou Aprendendo!', i);
    }
}

//decrecente
for (let i = 5; i >= 1; i--){
    if (i % 2 !== 0 ){ // somente impares
        console.log('Estou Aprendendo!', i);
    }
}

// While loop

let i = 5;

while ( i >= 1 ){
    if( i % 2 !== 0 ){
        console.log(i);
    }
    i--;
}

